package day02_variable;

public class Ex03 {

	public static void main(String[] args) {
	int num = 123;
	char ch = 'd';//'작은따옴표 문자 하나를 표현할 때 사용한다
	
	num = num + 10;
//123을 입력한 상태에서 10이 더해졌다 	
	ch = 'G';
	System.out.println("num : "+ num);
	System.out.println("ch : "+ch);
		
		//변수의 숫자는 언제든지 변경이 가능하다
	
		
	}
}
